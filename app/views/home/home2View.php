		function aceptoPoliticas(element) {
			var elementForm = $(element).parent().parent().parent().parent().parent();
			var idForm = elementForm.attr("id");

			if ($(element).is(":checked")) {
				$("#" + idForm + " .invalid-feedback").addClass('d-none');
